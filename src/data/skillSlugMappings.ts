// Helper functions
export function getEnglishSlug(spanishSlug: string): string | undefined {
  const entry = Object.entries(skillSlugMappings).find(([_, es]) => es === spanishSlug);
  return entry?.[0];
}

export function getSpanishSlug(englishSlug: string): string | undefined {
  return skillSlugMappings[englishSlug];
}

export function isSpanishSlug(slug: string): boolean {
  return Object.values(skillSlugMappings).includes(slug);
}

export function isEnglishSlug(slug: string): boolean {
  return slug in skillSlugMappings;
}

export function getAllSpanishSlugs(): string[] {
  return Object.values(skillSlugMappings);
}

export function getAllEnglishSlugs(): string[] {
  return Object.keys(skillSlugMappings);
}

export function getLanguageFromSlug(slug: string): 'en' | 'es' | undefined {
  if (isEnglishSlug(slug)) return 'en';
  if (isSpanishSlug(slug)) return 'es';
  return undefined;
}

export function normalizeToEnglishSlug(slug: string): string {
  if (isEnglishSlug(slug)) return slug;
  const english = getEnglishSlug(slug);
  return english || slug;
}

export default skillSlugMappings;
